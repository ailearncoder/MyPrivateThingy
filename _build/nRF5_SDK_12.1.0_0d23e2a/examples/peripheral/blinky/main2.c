void IIC_Stop()
{
	 nrf_gpio_cfg_output(SCL);
	 nrf_gpio_cfg_output(SDA);
   nrf_gpio_pin_write(SCL,low);
   //delay();
   nrf_gpio_pin_write(SDA,low);
   //delay();
   nrf_gpio_pin_write(SCL,high);
   //delay();
	 nrf_gpio_cfg_input(SDA,GPIO_PIN_CNF_PULL_Pullup);
   //nrf_gpio_pin_write(SDA,high);
   //delay();	 
}
/**********************************************
// IIC Write byte
**********************************************/
void Write_IIC_Byte(unsigned char IIC_Byte)
{
	nrf_gpio_cfg_output(SCL);
	nrf_gpio_cfg_output(SDA);
	unsigned char i;
	for(i=0;i<8;i++)		
	{
		if(IIC_Byte & 0x80)		//1?0?
		{		  
			nrf_gpio_pin_write(SDA,high);
		}
		else 
		{		  
			nrf_gpio_pin_write(SDA,low);
		}
		//delay();
		nrf_gpio_pin_write(SCL,high);
		//delay();
		nrf_gpio_pin_write(SCL,low);
		//delay();
		IIC_Byte<<=1;			//loop
	}
	nrf_gpio_cfg_input(SDA,GPIO_PIN_CNF_PULL_Pullup);
	//nrf_gpio_pin_write(SDA,high);
	//delay();
	nrf_gpio_pin_write(SCL,high);	
	//delay();
	while(nrf_gpio_pin_read(SDA))
		;//wait ack
	nrf_gpio_pin_write(SCL,low);
	//delay();
}
/**********************************************
// IIC Write Command
**********************************************/
void Write_IIC_Command(unsigned char IIC_Command)
{
   IIC_Start();
   Write_IIC_Byte(0x78);      //Slave address,SA0=0
   Write_IIC_Byte(0x00);			//write command
   Write_IIC_Byte(IIC_Command); 
   IIC_Stop();
}
/**********************************************
// IIC Write Data
**********************************************/
void Write_IIC_Data(unsigned char IIC_Data)
{
   IIC_Start();
   Write_IIC_Byte(0x78);			
   Write_IIC_Byte(0x40);			//write data
   Write_IIC_Byte(IIC_Data);
   IIC_Stop();
}
/********************************************
// fill_Picture
********************************************/
void fill_picture(unsigned char fill_Data)
{
	unsigned char m,n;
	for(m=0;m<8;m++)
	{
		Write_IIC_Command(0xb0+m);		//page0-page1
		Write_IIC_Command(0x00);		//low column start address
		Write_IIC_Command(0x10);		//high column start address
		for(n=0;n<128;n++)
		{
				Write_IIC_Data(fill_Data);
		}
	}
}
